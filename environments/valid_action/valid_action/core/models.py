# ---------- World ----------


class ValidActionWorld(_StrictModel):
    schema_version: str = "valid-action.world.v1"
    world_template_id: str
    seed: int
    difficulty: Difficulty
    action_date: date
    entities: list[Entity]
    people: list[Person]
    roles: list[RoleAppointment]
    bodies: list[GovernanceBody]
    security_classes: list[SecurityClass] = Field(default_factory=list)
    holder_positions: list[HolderPosition] = Field(default_factory=list)
    plan_capacities: list[EquityPlanCapacity] = Field(default_factory=list)
    records: list[LegalRecord] = Field(default_factory=list)
    requirements: Any  # RequirementGraph; forward-declared to avoid import cycle
    action_request: ActionRequest = Field(discriminator="action_type")
    oracle_solution: OracleSolution

    @model_validator(mode="after")
    def _check_unique_ids(self) -> "ValidActionWorld":
        entity_ids = {e.entity_id for e in self.entities}
        person_ids = {p.person_id for p in self.people}
        body_ids = {b.body_id for b in self.bodies}
        role_ids = {r.role_id for r in self.roles}
        class_ids = {s.class_id for s in self.security_classes}
        plan_ids = {p.plan_id for p in self.plan_capacities}
        record_ids = {r.record_id for r in self.records}

        for role in self.roles:
            if role.entity_id not in entity_ids:
                raise ValueError(f"role {role.role_id} references unknown entity")
            if role.person_id not in person_ids:
                raise ValueError(f"role {role.role_id} references unknown person")
            if role.body_id is not None and role.body_id not in body_ids:
                raise ValueError(f"role {role.role_id} references unknown body")
        for body in self.bodies:
            if body.entity_id not in entity_ids:
                raise ValueError(f"body {body.body_id} references unknown entity")
            for member_role_id in body.member_role_ids:
                if member_role_id not in role_ids:
                    raise ValueError(
                        f"body {body.body_id} member {member_role_id} unknown"
                    )
        for security in self.security_classes:
            if security.entity_id not in entity_ids:
                raise ValueError(f"security {security.class_id} unknown entity")
        for holder in self.holder_positions:
            if holder.class_id not in class_ids:
                raise ValueError(f"holder {holder.holder_id} unknown class")
            if holder.holder_id not in entity_ids and holder.holder_id not in person_ids:
                raise ValueError(f"holder {holder.holder_id} must be entity or person")
        for plan in self.plan_capacities:
            if plan.entity_id not in entity_ids:
                raise ValueError(f"plan {plan.plan_id} unknown entity")
        for record in self.records:
            if record.entity_id is not None and record.entity_id not in entity_ids:
                raise ValueError(f"record {record.record_id} unknown entity")
            for superseded in record.supersedes_record_ids:
                if superseded not in record_ids:
                    raise ValueError(
                        f"record {record.record_id} supersedes unknown {superseded}"
                    )
        return self

    def record_by_id(self, record_id: str) -> LegalRecord | None:
        for r in self.records:
            if r.record_id == record_id:
                return r
        return None

    def entity_by_id(self, entity_id: str) -> Entity | None:
        for e in self.entities:
            if e.entity_id == entity_id:
                return e
        return None

    def body_by_id(self, body_id: str) -> GovernanceBody | None:
        for b in self.bodies:
            if b.body_id == body_id:
                return b
        return None

    def person_by_id(self, person_id: str) -> Person | None:
        for p in self.people:
            if p.person_id == person_id:
                return p
        return None

    def role_by_id(self, role_id: str) -> RoleAppointment | None:
        for r in self.roles:
            if r.role_id == role_id:
                return r
        return None



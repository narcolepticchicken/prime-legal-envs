class EquityPlanCapacity(_StrictModel):
    plan_id: str
    entity_id: str
    total_reserved: int
    granted: int = Field(ge=0)
    cancelled_returned: int = Field(ge=0)

    @model_validator(mode="after")
    def _check_capacity(self) -> "EquityPlanCapacity":
        if self.total_reserved <= 0:
            raise ValueError("total_reserved must be positive")
        if self.granted < 0 or self.cancelled_returned < 0:
            raise ValueError("granted and cancelled_returned must be non-negative")
        if self.granted > self.total_reserved + self.cancelled_returned:
            raise ValueError("granted exceeds total_reserved + cancelled_returned")
        return self

    @property
    def available(self) -> int:
        return self.total_reserved + self.cancelled_returned - self.granted



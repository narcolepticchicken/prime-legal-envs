# ---------- Ownership and capacity (spec section 6.3) ----------


class SecurityClass(_StrictModel):
    class_id: str
    entity_id: str
    name: str
    authorized: int
    issued: int = Field(ge=0)
    reserved: int = Field(ge=0)
    votes_per_unit: Decimal = Decimal("1")

    @model_validator(mode="after")
    def _check_authorized(self) -> "SecurityClass":
        if self.authorized <= 0:
            raise ValueError("authorized must be positive")
        if self.issued + self.reserved > self.authorized:
            raise ValueError("issued + reserved exceeds authorized")
        if self.votes_per_unit < 0:
            raise ValueError("votes_per_unit must be non-negative")
        return self

    @property
    def available(self) -> int:
        return self.authorized - self.issued - self.reserved



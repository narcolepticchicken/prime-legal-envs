# ---------- Helpers ----------


_ID_PATTERN = re.compile(r"^[a-z][a-z0-9_]{2,63}$")


def _id_ok(value: str) -> bool:
    return bool(_ID_PATTERN.match(value))


def _positive_int(value: int) -> int:
    if value <= 0:
        raise ValueError(f"value must be positive, got {value}")
    return value


def _non_negative_int(value: int) -> int:
    if value < 0:
        raise ValueError(f"value must be non-negative, got {value}")
    return value


def _non_negative_decimal(value: Decimal) -> Decimal:
    if value < 0:
        raise ValueError(f"value must be non-negative, got {value}")
    return value


def _date_range(effective_from: date, effective_until: date | None) -> date | None:
    if effective_until is not None and effective_until < effective_from:
        raise ValueError("effective_until precedes effective_from")
    return effective_until


